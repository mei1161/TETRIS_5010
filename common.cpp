#include "common.h"
#include "direct3d.h"

////////////////////////////////////
// メンバ変数の実体を宣言
// ::しっかり参照してあげる
CommonStates* Common::state_;
////////////////////////////////////

////////////////////////////////////
// 初期化
////////////////////////////////////
bool Common::init()
{
    // CommonStatesクラスのメモリの確保
    state_ = new CommonStates( Direct3D::getDevice() );

    // メモリ確保チェック
    if( !state_ )
    {
        // エラー
        return false;
    }
    return true;
}